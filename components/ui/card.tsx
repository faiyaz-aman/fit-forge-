import * as React from "react";
import { cn } from "@/lib/utils";

export const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { hoverGlow?: boolean; glass?: boolean }
>(({ className, hoverGlow = false, glass = false, ...props }, ref) => {
  return (
    <div
      ref={ref}
      className={cn(
        "rounded-xl border border-border bg-card text-card-foreground shadow-sm transition-all duration-300",
        glass && "glass-panel",
        hoverGlow && "hover:shadow-[0_0_20px_rgba(0,255,136,0.06)] hover:border-primary/25",
        className
      )}
      {...props}
    />
  );
});
Card.displayName = "Card";

